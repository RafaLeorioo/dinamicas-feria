// ===== BOTONES DE CONTROL =====
resetBtn.onclick = () => {
  document.querySelectorAll(".button-group button.active").forEach(b => b.classList.remove("active"));
  selectedAges.clear();
  selectedTimes.clear();
  selectedSizes.clear();
  selectedPeople.clear();
  renderCards(activities);
};

toggleBtn.onclick = () => {
  filtersSection.classList.toggle("minimized");
  
  const textSpan = toggleBtn.querySelector(".toggle-text");
  const arrowSpan = toggleBtn.querySelector(".arrow");
  
  if (filtersSection.classList.contains("minimized")) {
    textSpan.textContent = "Maximizar";
    arrowSpan.textContent = "↓";
  } else {
    textSpan.textContent = "Minimizar";
    arrowSpan.textContent = "↑";
  }
};

// ===== MODAL =====
closeModal.onclick = () => modal.style.display = "none";
window.onclick = (event) => { if (event.target === modal) modal.style.display = "none"; };

function openModal(activity) {
  modalBody.innerHTML = `
    <h2>${activity.title}</h2>
    <p><span class="label">Material:</span> ${activity.material.join(", ")}</p>
    <p><span class="label">Edad:</span> ${activity.ages.join(", ")}</p>
    <p><span class="label">Duración:</span> ${activity.duration} min</p>
    <p><span class="label">Personas:</span> ${activity.people}</p>
    <p><span class="label">Máx. alumnos:</span> ${activity.maxStudents}</p>
    <p><span class="label">Tiempo mínimo visita:</span> ${activity.visit}</p>
    <hr style="margin: 20px 0; border: 0; border-top: 1px solid #eee;">
    <div style="line-height: 1.6;">${activity.fullDescription.replace(/\n/g, "<br>")}</div>
  `;
  modal.style.display = "flex";
}

// ===== RENDER =====
function renderCards(list) {
  cardsContainer.innerHTML = "";
  if (!list.length) {
    cardsContainer.innerHTML = "<p style='text-align:center; color:#888; grid-column: 1 / -1;'>No hay dinámicas que cumplan los filtros seleccionados.</p>";
    return;
  }

  list.forEach(a => {
    const card = document.createElement("div");
    card.className = "card";
    card.innerHTML = `
      <h2>${a.title}</h2>
      <p><span class="label">Material:</span> ${a.material.join(", ")}</p>
      <p><span class="label">Edad:</span> ${a.ages.join(", ")}</p>
      <p><span class="label">Duración:</span> ${a.duration} min</p>
      <p><span class="label">Personas:</span> ${a.people}</p>
      <p><span class="label">Máx. alumnos:</span> ${a.maxStudents}</p>
      <p><span class="label">Tiempo mínimo visita:</span> ${a.visit}</p>
      <button class="more-info-btn">Más información</button>
    `;
    card.querySelector(".more-info-btn").onclick = () => openModal(a);
    cardsContainer.appendChild(card);
  });
}

// ===== FILTRADO ESTRICTO =====
function applyFilters() {
  let result = activities.filter(a => {
    if (selectedAges.size > 0) {
      for (let age of selectedAges) {
        if (!a.ages.includes(age)) return false;
      }
    }
    if (selectedTimes.size > 0) {
      const dur = a.duration;
      for (let range of selectedTimes) {
        if (range === "short" && !(dur <= 3)) return false;
        if (range === "medium" && !(dur >= 4 && dur <= 7)) return false;
        if (range === "long" && !(dur >= 8)) return false;
      }
    }
    if (selectedSizes.size > 0) {
      const minAllowed = Math.min(...selectedSizes);
      if (a.maxStudents > minAllowed) return false;
    }
    if (selectedPeople.size > 0) {
      const hasOne = a.people.includes("1");
      const hasTwo = a.people.includes("2");
      for (let p of selectedPeople) {
        if (p === "1" && !hasOne) return false;
        if (p === "2" && !hasTwo) return false;
      }
    }
    return true;
  });

  renderCards(result);
}

